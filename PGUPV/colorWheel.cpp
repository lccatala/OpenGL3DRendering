std::vector<glm::vec4> ColorWheel::paletteVec4;

const glm::vec4 &ColorWheel::getColorVec4(unsigned char index) {
	if (paletteVec4.size() == 0) {
		paletteVec4.resize(256);
		for (int i = 0; i < 256; i++) {
			uchar *p = palette + 4 * i;
			paletteVec4[i] = glm::vec4(p[0] / 255.0f, p[1] / 255.0f, p[2] / 255.0f, 1.0f);
		}
	}

	return paletteVec4[index];
}
